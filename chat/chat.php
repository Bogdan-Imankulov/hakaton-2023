<?php

// Пример простого чат-бота на PHP

// Массив с вопросами и ответами
$responses = array(
    "привет" => "Привет! Как я могу вам помочь?",
    "как дела?" => "У меня все отлично, спасибо! А у вас?",
    "что делаешь?" => "Я просто чат-бот, отвечаю на вопросы.",
    "пока" => "До свидания! Если у вас возникнут вопросы, вы знаете, где меня найти."
);

// Функция для обработки вопросов и получения ответа
function getResponse($question, $responses) {
    // Приводим вопрос к нижнему регистру для упрощения сопоставления
    $question = strtolower($question);

    // Проверяем, есть ли точное совпадение в массиве ответов
    if (array_key_exists($question, $responses)) {
        return $responses[$question];
    } else {
        return "Извините, я не понял ваш вопрос. Попробуйте задать его по-другому.";
    }
}

// Получаем вопрос пользователя из GET-параметра
$userQuestion = isset($_GET['question']) ? $_GET['question'] : '';

// Получаем ответ от чат-бота
$botResponse = getResponse($userQuestion, $responses);

// Выводим ответ
echo $botResponse;

?>
